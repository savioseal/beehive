package beehive;

/**
 * ObjectType is an enum variable every IBoardable object must have.
 * 
 *
 */
public enum ObjectType {
	//	Every object on board has a type.
	/**
	 * Object is a Hive.
	 */
	HIVE,
	/**
	 * Object is a NectarBee.
	 */
	NECTAR_BEE,
	/**
	 * Object is a NectarPlant.
	 */
	NECTAR_PLANT,
	/**
	 * Object is an UselessPlant.
	 */
	USELESS_PLANT,
}
